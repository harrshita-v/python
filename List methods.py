#List methods
list = ["samsung", "nokia", "xiaomi", "apple"]
list.append("realme") #Adding an element to the list
print(list)
x = list.copy() #Copying the elements of a list
print(x)
x = list.count("xiaomi") #Return number corresponding to an element
print(x)
x = list.index("samsung") #Returns index value
print(x)
list.insert(1, "motorola")#inserts an element in the specified index value
print(list)
list.pop()#removes the last element of the list
print(list)
list.remove("nokia")#removes the specified element from the list
print(list)
list.reverse()#reverses the order of the elements
print(list)
list.sort()#sorts list
print(list)
list2= ['twix', 'mars', 'gems']# Adds the elements of two lists
list.extend(list2)
print(list)
list.clear()#Clears list
print(list)








